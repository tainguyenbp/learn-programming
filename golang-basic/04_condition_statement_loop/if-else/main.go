package main

import "fmt"

func main() {

	// if condition
	number := 10
	if number == 10 {
		fmt.Println("number = 10")
	}
	// if else condition
	if number < 10 {
		fmt.Println("number = 10")
	} else {
		fmt.Println("number = 10")
	}

	// if statement condition
	if a := 200; a > 100 {
		fmt.Println("a > 100")
	} else {
		fmt.Println("a = 100")
	}

}
